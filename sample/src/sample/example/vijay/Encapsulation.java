package sample;

public class Encapsulation {
	private String city;
	private int age;
	private String name;
	
	public String getName(){
		
		return name;
	
	}
	
	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public void setName(String name) {
		this.name=name;
		
		
	}
	
}
